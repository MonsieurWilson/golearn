// Fetch prints the content found at each specified URL.
package main

import (
	"fmt"
	"log"
	"net/http"
	"os"
)

func main() {
	for _, url := range os.Args[1:] {
		resp, err := http.Get(url)
		if err != nil {
			log.Fatalf("fetch: %v\n", err)
		}
		resp.Body.Close()
		fmt.Printf("Status: %s\n", resp.Status)
	}
}

